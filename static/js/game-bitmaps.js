function shipGunTipPos(ship, cx, cy) {
  if (ship === 'falcon') {
    const ox = cx - bmpW(FALCON_BMP) * PX / 2;
    const oy = cy - bmpH(FALCON_BMP) * PX / 2;
    const half = (PX - 1) / 2;
    return { lx: ox + FALCON_GUN_L * PX + half, rx: ox + FALCON_GUN_R * PX + half,
              nx: ox + 6 * PX + half, gy: oy + FALCON_GUN_ROW * PX + half, ny: oy + half };
  }
  if (ship === 'swordfish') {
    const ox = cx - bmpW(SWORDFISH_BMP) * PX / 2;
    const oy = cy - bmpH(SWORDFISH_BMP) * PX / 2;
    const half = (PX - 1) / 2;
    return { lx: ox + SWORDFISH_GUN_L * PX + half, rx: ox + SWORDFISH_GUN_R * PX + half,
              nx: ox + 7 * PX + half, gy: oy + SWORDFISH_GUN_ROW * PX + half, ny: oy + half };
  }
  if (ship === 'enterprise') {
    const ox = cx - bmpW(ENTERPRISE_BMP) * PX / 2;
    const oy = cy - bmpH(ENTERPRISE_BMP) * PX / 2;
    const half = (PX - 1) / 2;
    return { lx: ox + ENT_GUN_L * PX + half, rx: ox + ENT_GUN_R * PX + half,
              nx: ox + 6 * PX + half, gy: oy + ENT_GUN_ROW * PX + half, ny: oy + half };
  }
  if (ship === 'normandy') {
    const ox = cx - bmpW(NORMANDY_BMP) * PX / 2;
    const oy = cy - bmpH(NORMANDY_BMP) * PX / 2;
    const half = (PX - 1) / 2;
    return { lx: ox + 3 * PX + half, rx: ox + 9 * PX + half,
              nx: ox + 6 * PX + half, gy: oy + 9 * PX + half, ny: oy + 9 * PX + half };
  }
  if (ship === 'serenity') {
    const ox = cx - bmpW(SERENITY_BMP) * PX / 2;
    const oy = cy - bmpH(SERENITY_BMP) * PX / 2;
    const half = (PX - 1) / 2;
    return { lx: ox + SERENITY_GUN_L * PX + half, rx: ox + SERENITY_GUN_R * PX + half,
              nx: ox + 7 * PX + half, gy: oy + SERENITY_GUN_ROW * PX + half, ny: oy + half };
  }
  // Placeholder fallback for new ships: symmetric guns at row 2, ±2 cols from center
  const _newBmps = { pes: PES_BMP, inbound: INBOUND_BMP };
  if (_newBmps[ship]) {
    const bmp = _newBmps[ship];
    const ox = cx - bmpW(bmp) * PX / 2;
    const oy = cy - bmpH(bmp) * PX / 2;
    const half = (PX - 1) / 2;
    const mid = Math.floor(bmpW(bmp) / 2);
    return { lx: ox + (mid - 2) * PX + half, rx: ox + (mid + 2) * PX + half,
              nx: ox + mid * PX + half, gy: oy + 2 * PX + half, ny: oy + half };
  }
  return gunTipPos(cx, cy);
}

